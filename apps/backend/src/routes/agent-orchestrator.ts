import { Router, Response } from 'express';
import { requireUserAuth } from '../middleware/auth';
import { verifyBuildOwnership } from '../middleware/buildOwnership';
import { AuthenticatedRequest } from '../types';
import { getOrchestrator } from '../services/agentOrchestrator';
import { getSupabase } from '../services/supabase';
import { rateLimitAgentOrchestrator } from '../middleware/rateLimiting';
import { checkAgentOrchestratorCredits, deductOperationCredits } from '../middleware/costProtection';

const router = Router();

/**
 * POST /agent-orchestrator/builds/:buildId/start
 * Start agent orchestration for a build
 */
router.post('/builds/:buildId/start', requireUserAuth, rateLimitAgentOrchestrator, checkAgentOrchestratorCredits, verifyBuildOwnership, (async (req: AuthenticatedRequest, res: Response) => {
  try {
    if (!req.agent) {
      res.status(403).json({ error: 'No agent found for user' });
      return;
    }

    const { buildId } = req.params;

    // Get build (ownership already verified by middleware)
    const { data: build, error } = await getSupabase()
      .from('generated_apps')
      .select('*')
      .eq('id', buildId)
      .single();

    if (error || !build) {
      res.status(404).json({ error: 'Build not found' });
      return;
    }

    // Deduct credits upfront since orchestration runs in background
    if (req.user?.id && (req as any).requiredCredits) {
      await deductOperationCredits(
        req.user.id,
        (req as any).requiredCredits,
        'agent_orchestrator',
        { build_id: buildId }
      ).catch(err => console.error('Failed to deduct credits:', err));
    }

    // Start orchestration
    const orchestrator = getOrchestrator();

    // Don't await - let it run in background
    orchestrator.startBuild(buildId, build.prompt || build.name)
      .catch(err => {
        console.error('Orchestration error:', err);
      });

    res.json({
      success: true,
      message: 'Agent orchestration started',
    });
  } catch (error) {
    console.error('Start orchestration error:', error);
    res.status(500).json({
      error: 'Failed to start orchestration',
      message: error instanceof Error ? error.message : 'unknown error',
    });
  }
}) as any);

