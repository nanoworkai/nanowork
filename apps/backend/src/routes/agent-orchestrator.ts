/**
 * GET /agent-orchestrator/documents
 * Get all documents across all builds for the user
 */
router.get('/documents', requireUserAuth, (async (req: AuthenticatedRequest, res: Response) => {
  try {
    if (!req.agent) {
      res.status(403).json({ error: 'No agent found for user' });
      return;
    }

    // Get all builds for the user
    const { data: builds, error: buildsError } = await getSupabase()
      .from('generated_apps')
      .select('id, name')
      .eq('agent_id', req.agent.id);

    if (buildsError) {
      throw buildsError;
    }

    const buildIds = builds?.map(b => b.id) || [];

    if (buildIds.length === 0) {
      res.json({ documents: [] });
      return;
    }

    // Get all documents for these builds
    const { data: documents, error: docsError } = await getSupabase()
      .from('build_documents')
      .select('*')
      .in('build_id', buildIds)
      .order('created_at', { ascending: false });

    if (docsError) {
      throw docsError;
    }

    // Enrich documents with build names
    const enrichedDocuments = (documents || []).map(doc => {
      const build = builds?.find(b => b.id === doc.build_id);
      return {
        ...doc,
        build_name: build?.name || 'Unknown Build',
      };
    });

    res.json({
      documents: enrichedDocuments,
    });
  } catch (error) {
    console.error('Get all documents error:', error);
    res.status(500).json({
      error: 'Failed to get documents',
      message: error instanceof Error ? error.message : 'unknown error',
    });
  }
}) as any);

